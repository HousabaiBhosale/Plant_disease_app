  // ── Search ─────────────────────────────────────────────────
  static List<DiseaseInfo> search(String query) {
    if (query.trim().isEmpty) return all;
    final q = query.toLowerCase();
    return all.where((d) =>
      d.plantName.toLowerCase().contains(q) ||
      d.diseaseName.toLowerCase().contains(q) ||
      d.scientificName.toLowerCase().contains(q) ||
      d.type.toLowerCase().contains(q) ||
      d.description.toLowerCase().contains(q),
    ).toList();
  }

  // ── Filter by plant ────────────────────────────────────────
  static List<DiseaseInfo> filterByPlant(String plant) {
    if (plant == 'All') return all;
    return all.where((d) => d.plantName == plant).toList();
  }

  // ── Unique plant names ─────────────────────────────────────
  static List<String> get plants {
    final names = all.map((d) => d.plantName).toSet().toList();
    names.sort();
    return ['All', ...names];
  }
